    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String wynik = "";
        
        if( radioPan.isSelected() ) wynik += "Pan ";
        if( radioPani.isSelected() ) wynik += "Pani ";
        
        if( boxProf.isSelected() ) wynik += "Prof. ";
        if( boxDr.isSelected() ) wynik += "dr ";
        if( boxHab.isSelected() ) wynik += "hab. ";
        if( boxMgr.isSelected() ) wynik += "mgr ";
        if( boxInz.isSelected() ) wynik += "inż. ";
        
        Object imie = null;
        if(radioPan.isSelected()) imie = listaImionM.getSelectedValue();
        if(radioPani.isSelected()) imie = listaImionK.getSelectedValue();        
        if( imie != null) wynik += imie.toString() + " ";
        
        wynik += poleNazwisko.getText() + " ";
        
        poleTekstowe.setText(wynik);
    }//GEN-LAST:event_jButton1ActionPerformed

