    private String losujImie() {
        Random rnd = new Random();
        return tablicaImion[rnd.nextInt(tablicaImion.length)];
    }

    private String losujNazwisko() {
        Random rnd = new Random();
        return tablicaNazwisk[rnd.nextInt(tablicaNazwisk.length)];
    }

    private void dodajOsobe() {
        Number number = (Number) spinnerRokUrodzenia.getValue();
        int rok_urodzenia = 1981;
        if (number != null) {
            rok_urodzenia = number.intValue();
        }

        String napis = (radioButtonPan.isSelected() ? radioButtonPan.getText()
                : (radioButtonPani.isSelected() ? radioButtonPani.getText() : radioButtonKotek.getText()))
                + "  " + textFieldImie.getText() + " " + textFieldNazwisko.getText()
                + "  " + rok_urodzenia;

        modelLista.addElement(napis);
        int ile = widokTabela.getRowCount();
        Object[] wiersz = {ile + 1, textFieldImie.getText(), textFieldNazwisko.getText(), rok_urodzenia};
        modelTabela.addRow(wiersz);

    }

    private void losujOsobe() {
        textFieldImie.setText(losujImie());
        textFieldNazwisko.setText(losujNazwisko());
        spinnerRokUrodzenia.setValue(new Integer(1900 + new Random().nextInt(101)));

    }
